package com.oracle.mishoppingadmin.bean;


public class Collection {

  private long clid;
  private long uid;
  private long pid;


  public long getClid() {
    return clid;
  }

  public void setClid(long clid) {
    this.clid = clid;
  }


  public long getUid() {
    return uid;
  }

  public void setUid(long uid) {
    this.uid = uid;
  }


  public long getPid() {
    return pid;
  }

  public void setPid(long pid) {
    this.pid = pid;
  }

}
